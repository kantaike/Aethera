export const getStoredLanguage = (): Language => {
  if (typeof window === 'undefined') {
    return 'en';
  }

  const language = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return language === 'uk' ? 'uk' : 'en';
};

export const setStoredLanguage = (language: Language) => {
  if (typeof window === 'undefined') {
    return;
  }

  window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
  window.dispatchEvent(new Event(LANGUAGE_CHANGE_EVENT));
};

const subscribeToLanguage = (callback: () => void) => {
  if (typeof window === 'undefined') {
    return () => {};
  }

  const handleChange = () => callback();

  window.addEventListener(LANGUAGE_CHANGE_EVENT, handleChange);
  window.addEventListener('storage', handleChange);

  return () => {
    window.removeEventListener(LANGUAGE_CHANGE_EVENT, handleChange);
    window.removeEventListener('storage', handleChange);
  };
};

export const useLanguage = (): Language => {
  return useSyncExternalStore(subscribeToLanguage, getStoredLanguage, () => 'en');
};
